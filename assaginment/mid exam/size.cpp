#include<bits/stdc++.h>
using namespace std;

int main()
{
    int myInt;
    double myDouble;
    char myChar;

    cout << "Size of int: " << sizeof(myInt) << " bytes" <<endl;
    cout << "Size of double: " << sizeof(myDouble) << " bytes" <<endl;
    cout << "Size of char: " << sizeof(myChar) << " bytes" <<endl;
    return 0;
}